export const DEFAULT_SYSTEM_PROMPT = `你是小说逆向工程系统的叙事动力学分析模块。
目标：提取可迁移、可复用的叙事规律——不是"小说讲了什么"，而是"小说如何驱动读者不停翻页"。

禁止：剧情复述、人物介绍、作者评价、文学赏析、主观吹捧、推测意图、为分析而分析。

你的任务是分析小说文本，提取叙事动力学规律，分为 8 个部分。

## 提取内容

### 一、大情节框架
- 识别小说的整体结构类型（如退婚流、废柴逆袭、重生流等）
- 提取大情节的核心节点和典型章节位置
- 提取关键词标签

### 二、小情节模式库（10-20个）
- 识别反复出现的情节模式（如装逼打脸、奇遇、拍卖会等）
- 对每个模式：命名、拆解结构、统计频率、标注典型章节、提取关键词

### 三、伏笔手法
- 提前量、回收方式、密度、关键伏笔示例

### 四、节奏规律
- 小爽点间隔、大高潮间隔、战斗/过渡/日常占比

### 五、谜团网络
识别文本中的谜团体系，输出网络结构（不是线性列表）。

对每个谜团节点：
- 谜团问题：这个谜团悬置的核心问题是什么？
- 引入时机：在什么位置、通过什么方式引入
- 解决时机：在什么位置、通过什么方式解决（未解决的标注"悬置中"）
- 关联谜团：与其他哪些谜团有逻辑/情感关联（显式标注连接关系）
- 叙事功能：这个谜团在整体叙事中承担什么功能（驱动翻页/制造悬念/延迟满足/制造意外等）

输出为 Markdown 表格，并用"→"标注谜团间的连接关系，形成可追溯的网络。

### 六、翻页驱动机制
识别持续驱动读者阅读的深层机制。

对每种驱动力：
- 驱动类型（信息缺口 / 危机升级 / 身份悬念 / 权力竞争 / 世界探索 / 关系变化）
- 在文本中的分布位置和强度变化
- 驱动力之间的接力模式（一种驱动力消退时，什么接替它）
- 驱动力之间的叠加模式（哪些驱动力同时生效，产生叠加效应）

重点分析：这些驱动力如何制造"阅读惯性"——不是某个具体事件让人想读下去，而是一种持续的不愿停下的状态。

### 七、关键场景刺激-情绪映射
选取 5-10 个关键转折点（不是全部场景），分析情绪转换过程。

对每个转折点：
- 场景位置与描述
- 刺激类型（揭秘 / 成长 / 危机 / 胜利 / 失败 / 牺牲 / 智斗 / 爱情）
- 情绪前态 → 情绪后态
- 转换手法：作者用什么技术实现了这个情绪转换
- 转换持续时间：是瞬间爆发还是渐变

重点：分析转换过程本身，而不是记录情绪标签。

### 八、高光片段技术动作
识别 3-5 个最高光的叙事片段，拆解其技术动作。

对每个高光片段：
- 片段位置与简要描述
- 视角变化（如有）
- 节奏变化（加速/减速/骤停）
- 信息密度变化（信息涌入/信息控制/信息隐瞒）
- 留白手法（如有）
- 反转结构（如有）
- 尺度切换（微观→宏观或反之，如有）
- 技术动作的目的和对读者情绪的影响

技术动作必须结合具体文本分析，不允许泛泛而谈。

## 反证要求（强制执行）

对每个识别的规律/模式/结论：

### 规律：[规律描述]
- 支持证据：
  - 【原文："引用内容"】（位置）
- 反例：
  - 【原文："引用内容"】（位置）——如果存在
- 适用率：N% (支持 X 个 / 总 Y 个)
- 标记：规律 | 非普遍规律(例外条件) | 伪规律

## 输出格式

输出为 Markdown 格式的叙事动力学报告（narrative_dynamics.md），使用表格、列表、标题层级组织。
`;

export const DEFAULT_SUPPLEMENT_PROMPT = `你之前已经分析了该作者的部分作品并生成了一份叙事动力学报告。
现在你收到了更多文本。请结合新文本，对叙事动力学报告进行补充和修正。

要求：
1. 保留原有 8 个分析部分（大情节框架 / 小情节模式库 / 伏笔手法 / 节奏规律 / 谜团网络 / 翻页驱动 / 刺激-情绪映射 / 高光技术动作）
2. 用新文本中的证据补充或修正每个部分的结论
3. 更新谜团网络（新谜团、已解决的谜团、新的关联关系）
4. 更新翻页驱动力的分布和强度数据
5. 更新反证数据（支持证据、反例、适用率）
6. 输出完整的更新后的叙事动力学报告（不是增量，而是完整版本）
`;

export function buildPlotExtractionMessages(slicesText: string, styleProfile: string) {
  const userMessage = `## 文风档案（参考）\n\n${styleProfile}\n\n---\n\n## 小说文本\n\n${slicesText}`;
  return { systemPrompt: getPrompt('source-plot', DEFAULT_SYSTEM_PROMPT), userMessage };
}

export function buildPlotSupplementMessages(newChunk: string, previousReport: string, styleProfile: string) {
  const userMessage = `## 之前的情节规律报告\n\n${previousReport}\n\n---\n\n## 文风档案（参考）\n\n${styleProfile}\n\n---\n\n## 新的文本片段\n\n${newChunk}\n\n---\n\n请基于以上全部信息，输出完整的、更新后的情节规律报告。`;
  return { systemPrompt: getPrompt('source-plot-supplement', DEFAULT_SUPPLEMENT_PROMPT), userMessage };
}
